    </p>
    <p>
        2.7.7 开发者通过开放API获得的数据仅可服务于接入应用或网站，不得用于其它任何用途；
    </p>
    <p>
        2.7.8 业务范围为数据采集、分析并服务于其他第三方的开发者，仅可通过金桐开放商业数据API进行相关工作，同时签订《商业数据服务合作协议》；
    </p>
    <p>
        2.7.9 开发者负责其应用及服务的创作、开发、编辑、加工、修改、测试、运营及维护，并且自行承担相应的成本。
