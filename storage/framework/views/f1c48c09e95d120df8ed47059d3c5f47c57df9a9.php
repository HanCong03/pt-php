<div class="agreement-content">
    <h3>定义</h3>
    <p>
        如无特别说明，下列术语在本协议中的含义为：
    </p>
    <p>
        1.1 开发者，指经有效申请并经过金桐网投资有限公司同意，将其开发的各种应用接入基于金桐开放平台而向用户提供各种服务的，具备民事行为能力的个人、法人或其他组织。如无特别说明，也称“第三方”。
    </p>
    <p>
        1.2 应用，指由开发者开发的可向用户提供各种服务的应用程序，包括但不限于工具类服务、电子商务类服务、视频或音乐类服务等现存的各种服务及今后可能出现的各种服务，也称“授权程序”。
    </p>
    <p>
        1.3 网站，指由开发者开发的可向用户提供各种服务的网站，包括但不限于工具类服务、电子商务类服务、视频或音乐类服务等现存的各种服务及今后可能出现的各种服务，也称“授权网站”。
    </p>
    <p>
        1.4金桐开放平台，指由北京金桐网投资有限公司所拥有、控制、经营的开放平台。
    </p>
    <p>
        1.5金桐开放平台用户，指所有直接或间接使用金桐开放平台上授权应用和授权网站的企业或个人。以下简称为“用户”。
    </p>
    <p>
        1.6 用户数据，是指用户通过金桐开放平台提交的或因用户访问开放平台而生成的数据。“用户数据”是金桐开放平台的商业秘密。
    </p>
    <p>
        1.7 平台运营数据，是指在用户通过金桐开放平台使用平台服务、授权网站、开发者应用期间，用户所提交的或在金桐开放平台生成的或在授权网站和开发者应用生成的任何数据或信息，包括但不限于用户登录数据，交易数据等数据。“平台运营数据”是金桐开放的商业秘密。双方确认，平台运营数据和用户数据统称“数据”。
    </p>

    <h3>开放者的权利和义务</h3>

    <p>
        2.1 开发者保证：对开发者在金桐开放平台发布的应用、授权网站及利用金桐开放平台实施的活动真实、准确、完整、安全；不存在任何欺诈成份，不侵犯任何组织或个人的合法权益；不违反任何法律、法规、条例或规章；符合金桐开放的各项规范、规则及制度。对于开发者行为所造成的任何法律责任，由开发者单独承担，若由此给金桐公司及其关联公司造成任何损失，则开发者需足额赔偿。
    </p>
    <p>
        2.2 与金桐公司开展合作的开发者，其行为受本协议及金桐开放平台规则及金桐开放平台上公示的规则、制度、规范的约束。开发者必须合法使用金桐公司授予其应用的AppKey，不得违反本协议宗旨将该AppKey用于其他任何目的。AppKey是第三方应用接入金桐开放平台的凭证，开发者应当妥善保管AppKey及其密码，不得授权任何第三人使用其AppKey，并就该AppKey项下之一切活动负全部责任。因AppKey的使用行为导致金桐公司遭受损失的，开发者需足额赔偿。
    </p>
    <p>
        2.3 开发者应当具备从事本协议项下应用开发或提供服务的相关资质，开发者应当具备履行其基于本协议项下之义务的能力，且开发者履行相关义务不违反任何对开发者有约束力的法律文件，第三方开发应用或提供服务的具体资质和能力要求请参见《审核指南与流程》。
    </p>
    <p>
        2.4 开发者在其应用、授权网站设计上应当重视用户体验，尊重用户知情权、选择权，应用、网站应当坚持诚信原则，不误导、欺诈、混淆用户，尊重用户的隐私，不骚扰用户，不制造垃圾信息。
    </p>
    <p>
        2.5 开发者应用或服务需要收集用户数据的应当符合以下条件：
    </p>
    <p>
        2.5.1 开发者应用或服务需要收集用户数据的，必须事先获得用户的同意，且仅应当收集为应用程序运行及功能实现目的而必要的用户数据和用户在授权网站或开发者应用生成的数据或信息。开发者应当告知用户相关数据收集的目的、范围及使用方式，以保障用户的知情权；
    </p>
    <p>
        2.5.2 开发者应当就应用向金桐开放平台用户提供隐私保护政策，告知金桐开放平台用户该应用收集哪些用户数据，如何使用，是否会将用户数据传播或提交给他人等，以保障用户的知情权及选择权。隐私政策在应用的界面上应当显示给金桐开放平台用户。
    </p>
    <p>
        2.5.2 金桐开放平台各类数据均不能以政府舆情监控或类似理由申请，如确有该方面需要，需持相关委托函原件申请开通。
    </p>
    <p>
        2.5.4 开发者应当向用户提供修改、删除用户数据的方式，确保用户要求删除其用户数据时可通过该方式自行操作完成，并确保相关数据被完全删除；
    </p>
    <p>
        2.5.5 未经用户同意，开发者不得收集用户的隐私信息数据及其他金桐公司认为属于敏感信息范畴的数据；开发者不得收集或要求用户提供任何金桐开放帐号、密码；开发者不得收集或要求用户提供用户关系链、好友列表数据等；
    </p>
    <p>
        2.5.6 开发者不得以任何方式将用户数据给第三人，开发者在特定应用或服务中收集到的用户数据仅可以在该特定应用或服务中使用，不得以任何方式将收集到的用户数据转移或使用在该特定应用或服务之外。
    </p>
    <p>
        2.5.7 开发者应当仅获取为应用或服务运行及功能实现目的而必要的数据；
    </p>
    <p>
        2.5.8 开发者未经授权不得使用平台数据用于向金桐开放平台用户进行广告宣传使用；
    </p>
    <p>
        2.5.9 开发者不得私自以任何形式将平台数据提供给任何其他第三人使用；
    </p>
    <p>
        2.5.10 如果金桐公司认为开发者使用用户数据的方式，会损害金桐开放平台用户体验，金桐公司有权要求开发者删除相关数据并不得再以该方式使用用户数据。
    </p>
    <p>
        2.5.11 金桐公司有权限制或阻止开发者获取用户数据及平台数据。
    </p>
    <p>
        2.5.12 开发者应当遵守金桐公司关于用户数据的其他管理规则。
    </p>
    <p>
        2.5.13 金桐开放开放平台运营数据的全部权利，均归属金桐公司。未经金桐公司事先书面同意，开发者不得为本协议约定目的外使用该运营数据。
    </p>
    <p>
        2.5.14 开发者对外向合作伙伴提供应用运营数据，必须经金桐公司的书面同意，并使用金桐公司允许其使用的数据。
    </p>
    <p>
        2.5.15 一旦开发者停止使用开放平台或金桐公司基于任何原因终止对开发者在金桐开放开放平台的服务，开发者必须立即删除全部从金桐开放开放平台中获得的数据。
    </p>
    <p>
        2.5.16 开发者应当向用户提供客服支持，并提供有效联系方式确保用户可以进行投诉和咨询。
    </p>
    <p>
        2.6 开发者应就其应用及相关服务，事先向金桐开放平台用户提供其用户服务协议。该用户服务协议中应当明确其应用及相关服务由开发者向用户提供并由开发者对用户负责。若开发者未按要求向金桐公司提交，则视为开发者同意使用金桐公司提供的统一模板。基于该应用开发者与金桐开放平台用户之间发生的纠纷应当由开发者与金桐开放平台用户协商解决，与金桐公司无关。
    </p>
    <p>
        2.7 金桐开放平台的规则
    </p>
    <p>
        2.7.1 开发者不得在应用内宣传与本应用无关的任何其他信息，包括但不限于广告、其他的应用产品信息等。
    </p>
    <p>
        2.7.2 在没有获得金桐开放平台用户明示同意的情况下，开发者不得直接联系金桐开放平台用户，或向金桐开放平台用户发布任何商业广告及骚扰信息。
    </p>
    <p>
