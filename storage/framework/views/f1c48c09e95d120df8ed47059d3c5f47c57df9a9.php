    </p>
    <p>
        2.8 开发者上传应用应当承担以下责任
    </p>
    <p>
        2.8.1 开发者在发布应用或服务及对应用或服务进行编辑、修改和更新前应经金桐公司书面确认。若开发者未经金桐公司书面确认擅自对应用进行编辑、修改和更新的，金桐公司有权单方面解除本协议关系，协议解除后，造成金桐公司及相关用户损失的，开发者应予赔偿。
    </p>
    <p>
