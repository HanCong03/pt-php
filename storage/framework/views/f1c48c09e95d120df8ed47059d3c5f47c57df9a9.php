        开发者提供的应用及服务存在上述情况的，金桐公司有权根据自己的判断，随时单方暂停或终止与开发者的合作，由此给用户及开发者造成的损失，开发者应自行承担。
    </p>
    <p>
        2.8.3 开发者保证在授权程序和服务的运营过程中，由用户使用应用或服务产生的内容，包括但不限于留言、消息、评论、名称等，不违反授权区域法律、法规、政策的规定，不涉及有损中华人民共和国国家安全和利益的信息，也不涉及任何反动、色情、迷信、赌博、诽谤他人、侵权等违法信息，若有上述情形出现，开发者会及时删除或其采取他合理措施。开发者违反本条款所引起的任何争议、处罚和其他法律责任等，由其自行承担责任，给金桐公司造成损失的，开发者应予以足额赔偿。
