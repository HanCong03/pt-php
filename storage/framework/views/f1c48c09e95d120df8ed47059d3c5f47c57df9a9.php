    </p>
    <p>
        （11）自动将应用或服务窗口定向到其他网页；
    </p>
    <p>
        （12）未经授权获取对金桐开放平台的访问权
    </p>
    <p>
        开发者违反上述条款的，金桐公司有权根据其情节，对开发者做出警告、限制服务、关闭应用入口、终止服务等处理。开发者需负责赔偿因开发者违反上述任何条款而给用户、金桐公司或金桐公司的任何合作伙伴、关联企业所造成的一切损失。
    </p>
    <p>
        2.8.7 开发者应用在开放平台上运行期间，开发者需向用户提供与应用相关的客户服务并保证服务质量。客户服务形式包括但不限于通过明确且合理的方式告知用户客户服务渠道、提供即时通讯方式、电话等，且此客户服务的成本由开发者承担。若因客户服务侵害到他人权益并给他人造成损失的，则开发者应承担全部责任；如侵害到金桐公司权益并给金桐公司造成损失，则开发者应承担回复声誉的责任，并足额赔偿由此给金桐公司造成的一切损失。
    </p>
    <p>
        2.8.8 开发者应用或服务使用金桐公司提供的服务器环境，由此产生的所有费用（包括但不限于服务器、带宽成本）由开发者承担。
    </p>
    <p>
        2.9 开发者不应当以任何形式上传或使用存在侵权或违反法律法规规定的内容、应用、服务或程序。金桐公司有权基于自身判断将违背本协议约定/有侵权嫌疑的内容、应用、服务或程序做删除处理，开发者对上述处理方式有异议的，可以联系金桐公司并提供异议理由及相关证据。如果开发者多次上传侵权内容、应用、服务或程序的，金桐公司有权单方面决定停止向该开发者提供服务，并删除其开发者帐户。
    </p>
    <p>
        2.10 开发者同意接收来自金桐公司及其关联公司或合作伙伴发出的邮件、信息，包括但不限于关于用户利益、商业推广的信息等；开发者如需向金桐公司进行通知，应当通过金桐公司对外正式公布的通信地址、电子邮件地址等联系信息送达；开发者联系用户时应当使用其在应用界面中展示的统一的服务邮件地址。
